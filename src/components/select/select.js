import React from "react";

export default function Select({ onChange, options }) {
  return (
    <>
      <select onChange={(e) => onChange(e.target.value)}>
        {options.map((option) => (
          <option key={option.value} value={option.value}>
            {option.label}
          </option>
        ))}
      </select>
    </>
  );
}
